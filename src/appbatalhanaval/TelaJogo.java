    private void jogar(int casa) {
        AcaoBatalhaNaval acao = new AcaoBatalhaNaval(casa);
        JogadaBatalhaNaval jogada = new JogadaBatalhaNaval(acao);
        jogada.setJogador(jogador);
        jogada.jogar(acao);
        jogada.setEnderecoServidor(new Endereco("192.168.107.220", 864));
        jogada.setEnderecoJogador(new Endereco(servidor.getHost(), servidor.getPort()));
        jogada.setEnderecoDestinatario(jogada.getEnderecoServidor());
        ClienteBatalhaNaval cliente = new ClienteBatalhaNaval();
        cliente.enviarMensagem(jogada);
    }
    
