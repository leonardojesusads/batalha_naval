    private synchronized Component getComponentePeloNome(Container c, String nomeComponente) {
        Component[] comps = c.getComponents();
        for (int i = 0; i < comps.length; i++) {
            Component componente = comps[i];
            if (componente.getName() != null) {
                if (componente.getName().equals(nomeComponente)) {
                    return componente;
                }
            }
            Component retorno = getComponentePeloNome((Container) comps[i], nomeComponente);
            if (retorno != null) {
                return retorno;
            }
        }
        return null;
    }
    
    public void iniciarJogo() {
        jbConectar.disable();
    }
    
    public synchronized void renderizar(JogadaBatalhaNaval jogada) {
        System.out.println("Renderizando");
        AcaoBatalhaNaval acaoBatalhaNaval = (AcaoBatalhaNaval) jogada.getAcao();
        JButton botao = ((JButton) getComponentePeloNome(this, "casa" + String.valueOf(acaoBatalhaNaval.getCasa())));
        if (botao != null) {
            botao.setText(String.valueOf(acaoBatalhaNaval.getElemento()));
            botao.disable();
        } else {
            JOptionPane.showMessageDialog(null, "erro");
        }
    }
    
    public synchronized void atualizarPontuacao(JogadaBatalhaNaval jogada) {    
        AcaoBatalhaNaval acaoBatalhaNaval = (AcaoBatalhaNaval) jogada.getAcao();    
        jlPontuacao.setText(String.valueOf(Integer.parseInt(jlPontuacao.getText()) + acaoBatalhaNaval.getElemento()));
    }

